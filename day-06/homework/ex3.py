

distance = float(input("Enter distance from school to your home (in KMs): "))

in_meters = distance * 1000
in_centimeters = in_meters * 100
in_millimeters = in_centimeters * 10

print(f"Distance in Meters is: {in_meters}")
print(f"Distance in Centimetres is: {in_centimeters}")
print(f"Distance in Milimetres is {in_millimeters}") 